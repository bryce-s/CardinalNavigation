using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CardinalNavigation
{
    class Coordinate
    {
        public int x;
        public int y;
        public int width;
        public int height;

        public Coordinate(int x_in, int y_in, int width_in, int height_in)
        {
            x = x_in;
            y = y_in;
            width = width_in;
            height = height_in;
        }

    }
}
